    "def classify_summary(results):\n",
    "    stress = sum(p for _, p in results)\n",
    "    total = len(results)\n",
    "    non_stress = total - stress\n",
    "    ratio = stress / total if total else 0\n",
    "\n",
    "    if ratio <= 0.2:\n",
    "        status = \"Healthy / Stable\"\n",
    "    elif ratio <= 0.5:\n",
    "        status = \"Mild Stress\"\n",
    "    elif ratio <= 0.75:\n",
    "        status = \"Moderate Stress\"\n",
    "    else:\n",
    "        status = \"High Stress\"\n",
    "\n",
    "    return {\n",
    "        \"Total Sentences\": total,\n",
    "        \"Stress\": stress,\n",
    "        \"Non-Stress\": non_stress,\n",
    "        \"Stress Ratio\": f\"{ratio:.1%}\",\n",
    "        \"Mental Health Status\": status\n",
    "    }\n",
    "\n",
    "def pie_chart(stress, non_stress):\n",
    "    fig, ax = plt.subplots()\n",
    "    ax.pie([non_stress, stress], labels=[\"Non-Stress\", \"Stress\"],\n",
    "           autopct=\"%1.1f%%\", startangle=90, colors=[\"#66b3ff\", \"#ff6666\"])\n",
    "    ax.axis('equal')\n",
    "    st.pyplot(fig)\n",
    "\n",
    "def generate_pdf(sentences, summary):\n",
